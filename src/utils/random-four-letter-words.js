function getRandomFourLetterWord() {
  return arraySample(FOUR_LETTER_WORDS);
}

function generate(amount = 1) {
  if (amount > FOUR_LETTER_WORDS.length) {
    throw new Error(
      `amount can not be greater than ${FOUR_LETTER_WORDS.length}`
    );
  }

  const randomFourLetterWords = new Set();
  while (randomFourLetterWords.size !== amount) {
    randomFourLetterWords.add(getRandomFourLetterWord());
  }
  return [...randomFourLetterWords];
}

